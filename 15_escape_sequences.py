#escape sequences 
story = "sahil is good boy.\nHe is very good"  # gives a new line
print(story)

print("tab sequence--------")
story = "sahil is good boy.\nHe\t is very good"  #gives a tab after He
print(story)

print("\r--------------")
story = "sahil is good boy.\nHe is ve\ry good"
print(story)

print("backslash\\--------------")
story = "sahil is good\\ boy.\nHe is very good"   # have to use two backslash for single backslash
print(story)