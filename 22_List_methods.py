# Sort method
l1 = [1, 5, 8, 5, 93, 67]
print("Before shorting: ", l1)
l1.sort()
print("After shorting: ", l1)

# reverse method
l1 = [1, 5, 8, 5, 93, 67]
print("Before reverse: ", l1)
l1.reverse()
print("After reverse: ", l1)

# append method
l1 = [1, 5, 8, 5, 93, 67]
print("Before append: ", l1)
l1.append(88)  # appends at the end of the list
l1.append(99)  # appends at the end of the list
print("After append: ", l1)

# insert method
l1 = [1, 5, 8, 5, 93, 67]
print("Before insert: ", l1)
l1.insert(0, 684)
print("After insert: ", l1)

# pop method
l1 = [1, 5, 8, 5, 93, 67]
print("Before pop: ", l1)
l1.pop(4)  # remove at 4 index
print("After pop: ", l1)


# remove method
l1 = [1, 5, 8, 5, 93, 67]
print("Before remove: ", l1)
l1.remove(8)  # remove 8 from the list
print("After remove: ", l1)
