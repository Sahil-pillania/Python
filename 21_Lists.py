# Lists
# create a list using []
a = [1, 2, 3, 4, 5, 6, 7, 8]
print(a)  # list print

# Accessing element values using index
print(a[2])

# Changing elements
print("changing elements")
a = [1, 2, 3, 4, 5, 6]
a[3] = 56
print(a)  # list print

# We can create a list with items of different types.
c = [89, "Sahil", False, 4.2]
print(c)

# List slicing
print("List slicing")
name = ["Sahil", "Rohan", "Arun", "Tom", "Somvir", 69]
print(name[0:4])  # 4 is excluded
print(name[3:])  # execute all elements from 3
