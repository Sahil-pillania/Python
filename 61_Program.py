# Program to greet a user with "Good day" using Functions
def greet(name):   # function of greeting
    print("Good Day, "+ name)

def mySum(num1, num2):   # function of sum
    return num1 + num2

greet("Sahil")       #output
s = mySum(6, 32)     # transfering mysum() value to s
print(s)             #output