# Condition expression

#if elif else expression
a = 15

if(a>3):
    print("The value of a is greater than 3 ")
elif(a>7):
    print("The value of a is greater than 7")
elif(a>19):
    print("The value of a is greater than 19")
else:
    print("The value is not greater than 3 or 7")
    # will execute only one condition at a time which come true first
print("Done")


